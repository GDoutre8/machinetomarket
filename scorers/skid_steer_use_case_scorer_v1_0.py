def _compute_frame_class(record: MachineRecord) -> tuple[str, str]:
    """
    Classify skid steer into small / medium / large frame based on ROC.

    Frame class drives use-case gating (small machines are GATED OUT of
    truck loading and heavy material handling) and profile-based boosts
    (large machines get production-work scoring advantages).

    Boundaries:
      small_frame:  ROC < 1,750 lb  (Cat 226D3, Bobcat S510, Gehl R105)
      medium_frame: ROC 1,750-2,500 lb  (JD 320G, Bobcat S650, Cat 246D3)
      large_frame:  ROC > 2,500 lb  (Bobcat S770, Case SV340, JD 332G)

    When ROC is unknown, falls back to capability class (A→small, B→medium, C/D→large).
    S76 crossover (ROC ~2,900) classifies as large_frame by ROC but carries
    medium-frame access characteristics — handled separately via s76_crossover condition.
    """
    roc = _safe_get(record, "rated_operating_capacity_lbs") or 0

    if roc > 0:
        if roc < 1750:
            return "small", "Small Frame (ROC < 1,750 lb) — compact, residential and farm machines"
        elif roc <= 2500:
            return "medium", "Medium Frame (ROC 1,750–2,500 lb) — general construction and ag use"
        else:
            return "large", "Large Frame (ROC > 2,500 lb) — production machines, truck loading, heavy handling"

    # ROC unknown: fall back to capability class
    cap_class, _ = _compute_capability_class(record)
    if cap_class == "A":
        return "small", "Small Frame (estimated from HP/weight — ROC not on record)"
    if cap_class == "B":
        return "medium", "Medium Frame (estimated from HP/weight — ROC not on record)"
    return "large", "Large Frame (estimated from HP/weight — ROC not on record)"


