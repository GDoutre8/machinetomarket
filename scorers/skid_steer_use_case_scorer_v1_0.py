"""
skid_steer_use_case_scorer.py
==============================
MTM Listings -- Skid Steer Use Case Scoring Engine
Derived from: Skid Steer Use Case Scoring Engine Master Framework v1.0

Scores a wheeled skid steer record against 13 real-world use cases using
weighted spec-driven rules. Returns capability class, ranked use cases,
attachment compatibility, listing value highlights, and scoring flags.

Key differences from CTL scorer:
  - Tire condition replaces track condition
  - Vertical vs. radial lift is a MAJOR score driver for skid steers
    (more differentiated than in CTLs where most are radial)
  - Concrete / flatwork prep added (important skid steer use case)
  - Warehouse / yard use added (skid steers dominate indoors)
  - Agriculture / farm use added
  - Hinge pin height thresholds are slightly lower than CTLs
    (skid steers are lighter; OEM hinge pins typically 5-10 in lower)
  - High flow thresholds mirror CTL -- same attachment physics

SECTION REFERENCE
-----------------
Section 1: Capability Classes (A / B / C / D)
Section 2: Attachment Hydraulic Demand Tiers
Section 3: Use Case → Spec Driver Mapping
Section 4: Vertical vs. Radial Lift Logic
Section 5: Listing Value Drivers
Section 6: Buyer Types by Machine Class

Usage:
    from skid_steer_use_case_scorer import score_skid_steer
    result = score_skid_steer(machine_record)
"""

from dataclasses import dataclass, field


# ---------------------------------------------------------------------------
# DATA STRUCTURES
# ---------------------------------------------------------------------------

@dataclass
class MachineRecord:
    """
    Normalized skid steer machine input record.
    All fields are optional -- scorer handles None gracefully.
    Field names match MTM registry schema exactly.
    """
    # Core specs
    horsepower_hp: float | None = None
    rated_operating_capacity_lbs: float | None = None
    operating_weight_lbs: float | None = None
    aux_flow_standard_gpm: float | None = None
    aux_flow_high_gpm: float | None = None
    hydraulic_pressure_standard_psi: float | None = None
    hydraulic_pressure_high_psi: float | None = None
    bucket_hinge_pin_height_in: float | None = None

    # Feature flags
    high_flow_available: bool | None = None
    two_speed_available: bool | None = None
    enclosed_cab_available: bool | None = None
    ride_control_available: bool | None = None
    joystick_controls_available: bool | None = None   # ISO/H-pattern vs joystick

    # Lift geometry: "radial" | "vertical"
    lift_path: str | None = None

    # Condition / market fields
    brand: str | None = None
    model: str | None = None                  # e.g. "S770", "226D3" -- used for crossover detection
    hours: float | None = None
    tire_condition_pct: float | None = None   # 0-100; None = unknown
    dealer_type: str | None = None            # e.g. "ag-pro", "farm", "construction" -- profile signal


@dataclass
class UseCaseScore:
    """Score result for a single use case."""
    use_case: str
    score: int                               # 0-100
    label: str                               # Excellent / Good / Fair / Poor / Not Recommended
    reasons: list[str] = field(default_factory=list)
    flags: list[str] = field(default_factory=list)
    base_score_before_adjustments: int = 0
    applied_caps: list[str] = field(default_factory=list)
    applied_penalties: list[str] = field(default_factory=list)
    applied_boosts: list[str] = field(default_factory=list)
    triggered_hard_gates: list[str] = field(default_factory=list)


@dataclass
class ScorerResult:
    """Full scoring result returned by score_skid_steer()."""
    capability_class: str                    # "A" | "B" | "C" | "D"
    capability_class_label: str
    frame_class: str                         # "small" | "medium" | "large"
    frame_class_label: str                   # human-readable frame class description
    machine_profile: str                     # "production" | "yard" | "ag" | "general"
    top_use_cases: list[UseCaseScore]        # top 3 scored use cases
    all_use_cases: list[UseCaseScore]        # all 13, sorted descending by score
    attachment_compatibility: dict[str, dict]
    listing_value_highlights: list[str]
    scoring_flags: list[str]
    limitations: list[str]
    best_for_summary: str
    not_ideal_for_summary: str
    buyer_type_summary: str
    confidence_level: str             # "High" | "Medium" | "Low"


