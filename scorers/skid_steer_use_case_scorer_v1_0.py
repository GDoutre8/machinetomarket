# SECTION 1: CAPABILITY CLASS COMPUTATION
# ---------------------------------------------------------------------------

def _compute_capability_class(record: MachineRecord) -> tuple[str, str]:
    """
    Determine skid steer capability class (A/B/C/D) from actual specs.

    SECTION 6: Buyer Type is embedded in class labels:
      A -- Small: Landscapers, farms, property maintenance, tight-access
      B -- Mid: General contractors, concrete, demo, snow, agriculture
      C -- Large: Heavy construction, demolition, road prep, production loading
      D -- High-Flow: Asphalt/cold planing, stump grinding, land clearing crews

    Classification logic:
      1. ROC is the primary driver (same philosophy as CTL scorer).
      2. Weight can step a class UP when materially above the band ceiling.
         Weight never steps DOWN.
      3. HP is fallback when ROC is unknown.
      4. Class D requires BOTH confirmed HF GPM >= 36 AND HP >= 85.
    """
    roc    = _safe_get(record, "rated_operating_capacity_lbs") or 0
    hp     = _safe_get(record, "horsepower_hp") or 0
    weight = _safe_get(record, "operating_weight_lbs") or 0
    hf_gpm = _effective_high_flow_gpm(record) or 0

    class_labels: dict[str, str] = {
        "A": "Small Skid Steer -- Landscaping / Farm / Tight-Access",
        "B": "Mid Skid Steer -- General Construction / Concrete / Snow",
        "C": "Large Skid Steer -- Heavy Construction / Production Loading",
        "D": "High-Flow / Specialty Skid Steer -- Asphalt / Mulching / Stump",
    }
    class_order = ["A", "B", "C"]
    d_rules = RULES["class_boundaries"]["D"]

    # Class D: both confirmed GPM and HP must clear their thresholds
    if hf_gpm >= d_rules["high_flow_min_gpm"] and hp >= d_rules["hp_min"]:
        return "D", class_labels["D"]

    def _roc_class(v: float) -> str:
        if v <= RULES["class_boundaries"]["A"]["roc_max"]:
            return "A"
        if v <= RULES["class_boundaries"]["B"]["roc_max"]:
            return "B"
        return "C"

    # Step 1: base class from ROC
    if roc > 0:
        base_class = _roc_class(roc)
    elif weight > RULES["class_boundaries"]["B"]["weight_max"]:
        base_class = "C"
    elif weight > RULES["class_boundaries"]["A"]["weight_max"]:
        base_class = "B"
    elif hp > RULES["class_boundaries"]["B"]["hp_max"]:
        base_class = "C"
    elif hp > RULES["class_boundaries"]["A"]["hp_max"]:
        base_class = "B"
    else:
        base_class = "B"

    # Step 2: secondary weight step-up (never down)
    if weight > 0 and base_class in class_order:
        adj_pct = RULES["weight_adjustment_pct"]
        idx = class_order.index(base_class)
        weight_max = RULES["class_boundaries"][base_class]["weight_max"]
        prev_weight_max = (
            RULES["class_boundaries"][class_order[idx - 1]]["weight_max"]
            if idx > 0 else 0
        )
        band_size = weight_max - prev_weight_max
        upper_threshold = weight_max + (band_size * adj_pct)

        if weight > upper_threshold and idx < len(class_order) - 1:
            base_class = class_order[idx + 1]

    return base_class, class_labels[base_class]


