def _compute_attachment_compatibility(record: MachineRecord) -> dict[str, dict]:
    """
    Classify Tier 1/2/3 attachment compatibility based on hydraulic specs.

    SECTION 2 REFERENCE DATA:
      Tier 1 (8-17 GPM): Universal -- all skid steers
        Bucket, pallet forks, grapple, broom/sweeper, box blade, snow blade,
        landscape rake, hay spear, light auger (4-6" soft soil), pallet jack
      Tier 2 (17-24 GPM std, 2800+ PSI):
        Auger (8-12" dia), chain trencher, hydraulic breaker, soil tiller/conditioner,
        vibratory plate compactor, backhoe attachment, angle broom, mini boom
      Tier 3 (28-42 GPM HIGH FLOW required, 3000+ PSI):
        Cold planer/milling head, forestry mulcher, brush/rotary cutter,
        stump grinder, large rock trencher chain, wood chipper, masticator
    """
    std_flow     = _safe_get(record, "aux_flow_standard_gpm") or 0
    hf_gpm       = _effective_high_flow_gpm(record) or 0
    pressure     = _safe_get(record, "hydraulic_pressure_standard_psi") or 0
    hf_available = _safe_get(record, "high_flow_available")
    hf_unconf    = _hf_present_unconfirmed(record)
    h = RULES["hydraulic"]

    # Tier 2
    if std_flow >= h["tier2_min_standard_gpm"] and pressure >= h["pressure_standard_min_psi"]:
        tier2_ok   = True
        tier2_note = "Compatible -- auger (8-12\"), trencher, breaker, tiller, compactor"
    elif std_flow >= 14:
        tier2_ok   = True
        tier2_note = ("Marginal -- light Tier 2 work possible; "
                      "reduced performance on demanding attachments")
    else:
        tier2_ok   = False
        tier2_note = "Not recommended -- standard flow too low for sustained Tier 2 attachments"

    # Tier 3
    if hf_available is False and std_flow < h["tier3_hard_disqualifier_gpm"]:
        tier3_ok   = False
        tier3_note = ("Not compatible -- no high-flow package; "
                      "cold planer, mulcher, and rotary cutter not supported")
    elif hf_unconf:
        tier3_ok   = True
