# CONFIDENCE LEVEL
# ---------------------------------------------------------------------------

def _compute_confidence_level(record: MachineRecord) -> str:
    """
    Rate scoring confidence based on how many of 7 core specs are present.
    Lift path is a critical spec — its absence drops confidence one tier
    regardless of other spec count (it affects 8 of 13 use case scores).

    Core specs checked:
      horsepower_hp, rated_operating_capacity_lbs, operating_weight_lbs,
      aux_flow_standard_gpm, hydraulic_pressure_standard_psi,
      bucket_hinge_pin_height_in, lift_path

    6-7 specs present -> "High"
    4-5 specs present -> "Medium"
    0-3 specs present -> "Low"

    Lift path missing: result drops one tier (High->Medium, Medium->Low).
    """
    core_fields = [
        "horsepower_hp",
        "rated_operating_capacity_lbs",
        "operating_weight_lbs",
        "aux_flow_standard_gpm",
        "hydraulic_pressure_standard_psi",
        "bucket_hinge_pin_height_in",
        "lift_path",
    ]
    present = sum(
        1 for f in core_fields
        if (_safe_get(record, f) is not None
            and str(_safe_get(record, f)).strip() != "")
    )
    if present >= 6:
        raw = "High"
    elif present >= 4:
        raw = "Medium"
    else:
        raw = "Low"

    # Lift path missing: drop one tier -- it affects 8 of 13 use case scores
    lift_path_missing = not (_safe_get(record, "lift_path") or "").strip()
    if lift_path_missing:
        if raw == "High":
            return "Medium"
        return "Low"

    return raw


