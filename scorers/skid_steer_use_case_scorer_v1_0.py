    hf_unconf = _hf_present_unconfirmed(record)
    cab       = _safe_get(record, "enclosed_cab_available")
    two_spd   = _safe_get(record, "two_speed_available")
    lift      = (_safe_get(record, "lift_path") or "").lower()
    hp        = _safe_get(record, "horsepower_hp") or 0
    roc       = _safe_get(record, "rated_operating_capacity_lbs") or 0
    pressure  = _safe_get(record, "hydraulic_pressure_standard_psi")
    tire_pct  = _safe_get(record, "tire_condition_pct")

    if hf is False:
        flags.append(
            "NO HIGH-FLOW: Tier 3 attachments (cold planer, mulcher, rotary cutter, "
            "stump grinder) not supported"
        )
        limitations.append("cold planing, forestry mulching, and production-rate brush cutting")
    elif hf is None and _safe_get(record, "aux_flow_high_gpm") is None:
        flags.append(
            "HIGH-FLOW STATUS UNKNOWN — Tier 3 attachment compatibility cannot be confirmed; "
            "do not claim cold planer, stump grinder, or brush cutter capability without "
            "verifying high-flow status from OEM spec sheet"
        )
    elif hf_unconf:
        flags.append(
            "HIGH-FLOW UNCONFIRMED GPM: High-flow package present but aux_flow_high_gpm "
            "not on record -- verify before advertising Tier 3 capability; "
            "Tier 3 use case scores capped"
        )
    elif hf_gpm and hf_gpm < 30:
        flags.append(
            f"MARGINAL HIGH-FLOW: {hf_gpm:.0f} GPM -- Tier 3 work possible "
            "but at reduced performance"
        )

    if cab is False:
        flags.append(
            "OPEN CAB (ROPS): Operator exposure limits use cases; "
            "demolition and debris-generating work not recommended"
        )
        limitations.append("debris-intensive demolition and forestry work without enclosed cab")

    if two_spd is False:
        flags.append(
            "SINGLE-SPEED: Productivity limited on commercial snow routes "
            "and large multi-zone sites"
        )
        limitations.append("commercial-scale snow removal and frequent multi-zone repositioning")

    if lift == "radial":
        flags.append(
            "RADIAL LIFT: Lower max hinge pin height than vertical lift; "
            "truck-height loading requires careful machine and dump height matching"
        )
    elif lift == "vertical":
        flags.append(
            "VERTICAL LIFT: Preferred for truck loading and elevated forks; "
            "radial lift buyers (grading, digging) may prefer otherwise"
        )

    if 0 < hp < 55:
        flags.append(
            "LOWER HP: Sustained production-rate attachment work may stress engine"
        )
        limitations.append("sustained high-demand hydraulic attachment work at lower HP")

    if 0 < roc < 1500:
        flags.append("LOWER ROC: Heavy material handling will require additional passes")
        limitations.append("production-rate material handling and truck loading at this ROC")

    if 0 < roc < 1200:
        flags.append(
            "LIMITED BALE HANDLING CAPACITY: ROC below 1,200 lb -- "
            "round bales typically weigh 1,500-2,000 lb; "
            "this machine cannot safely handle standard round bales"
        )

    if pressure is not None and pressure < 2800:
        flags.append(
            f"LOW PRESSURE ({int(pressure)} PSI): "
            "Rock trenching and hydraulic breakers may underperform"
        )
        limitations.append("rock trenching and hydraulic breaker work at this pressure")

    if tire_pct is not None and tire_pct < 40:
        flags.append(
            f"WORN TIRES ({int(tire_pct)}%): "
            "Major upcoming expense -- disclose upfront and price accordingly; "
            "typical replacement $800-2,500 depending on tire size"
        )

    hard_fails = [
        uc.use_case for uc in all_scores
        if uc.score == 0 and uc.triggered_hard_gates
    ]
    if hard_fails:
        flags.append(f"HARD DISQUALIFIED: {', '.join(hard_fails)}")

    # Missing-spec flags -- appended last so operational flags appear first
    if _safe_get(record, "rated_operating_capacity_lbs") is None:
        flags.append(
            "SPEC MISSING: ROC not on record -- capacity-based scoring less accurate"
        )
    if _safe_get(record, "horsepower_hp") is None:
        flags.append(
            "SPEC MISSING: Horsepower not on record"
        )
    if _safe_get(record, "aux_flow_standard_gpm") is None:
        flags.append(
            "SPEC MISSING: Standard hydraulic flow not on record -- attachment scoring less accurate"
        )
    if _safe_get(record, "bucket_hinge_pin_height_in") is None:
        flags.append(
            "SPEC MISSING: Hinge pin height not on record -- truck loading scoring less accurate"
        )
    if not (_safe_get(record, "lift_path") or "").strip():
        flags.append(
            "SPEC MISSING: Lift path unknown -- vertical vs radial scoring affected"
        )
    if _safe_get(record, "hydraulic_pressure_standard_psi") is None:
        flags.append(
            "SPEC MISSING: Hydraulic pressure not on record -- breaker/trenching scoring less accurate"
        )

    return flags, limitations


