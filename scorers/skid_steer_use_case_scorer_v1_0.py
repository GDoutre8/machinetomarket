def _compute_listing_value_highlights(record: MachineRecord) -> list[str]:
    """
    Generate a prioritized list of listing-worthy features for skid steers.

    SECTION 5 RANKING (dealer demand impact):
      1. Low hours -- universal premium trigger
      2. Brand tier -- Cat/Deere/Bobcat command 10-20% premium
      3. Vertical lift -- critical for loading-heavy buyers
      4. High flow -- opens attachment market significantly
      5. Enclosed cab -- all-weather use, comfort premium
      6. 2-speed -- essential for commercial snow / large-area work
      7. Ride control -- comfort and spill reduction
      8. Joystick controls -- operator preference premium
      9. Good tires -- visible buyer concern (immediate cost estimate)
     10. Strong ROC / HP -- production buyers compare specs
    """
    highlights: list[str] = []

    hours     = _safe_get(record, "hours")
    brand     = (_safe_get(record, "brand") or "").lower().strip()
    hf        = _safe_get(record, "high_flow_available")
    hf_gpm    = _effective_high_flow_gpm(record)
    hf_unconf = _hf_present_unconfirmed(record)
    cab       = _safe_get(record, "enclosed_cab_available")
    lift      = (_safe_get(record, "lift_path") or "").lower()
    two_spd   = _safe_get(record, "two_speed_available")
    ride      = _safe_get(record, "ride_control_available")
    joystick  = _safe_get(record, "joystick_controls_available")
    tire_pct  = _safe_get(record, "tire_condition_pct")
    roc       = _safe_get(record, "rated_operating_capacity_lbs")
    hp        = _safe_get(record, "horsepower_hp")

    # Hours (Rank 1)
    if hours is not None:
        for threshold, label in RULES["hours_labels"]:
            if threshold is None or hours <= threshold:
                if hours <= 1500:
                    highlights.append(
                        f"[HIGH VALUE] {label} ({int(hours):,} hrs) -- "
                        "lead with hours in title"
                    )
                elif hours <= 3000:
                    highlights.append(f"[NOTE] {int(hours):,} hrs -- solid working machine")
                else:
                    highlights.append(
                        f"[CAUTION] {int(hours):,} hrs -- price accordingly; "
                        "emphasize recent service history if available"
                    )
                break

    # Brand (Rank 2)
    for tier_key, tier_data in RULES["brand_tiers"].items():
        if brand in tier_data["brands"]:
            if tier_key == "tier1":
                highlights.append(
                    f"[HIGH VALUE] {tier_data['label']} ({brand.title()}) -- "
                    "lead with brand name in title; commands resale premium"
                )
            elif tier_key == "tier2":
                highlights.append(
                    f"[NOTE] {tier_data['label']} ({brand.title()}) -- "
                    "mention dealer support network and parts availability"
                )
            else:
                highlights.append(
                    f"[NOTE] {tier_data['label']} ({brand.title()}) -- "
                    "emphasize specs and condition over brand name"
                )
            break

    # Lift path (Rank 3 -- skid steer buyers care about this more than CTL buyers)
    if lift == "vertical":
        highlights.append(
            "[HIGH VALUE] Vertical Lift -- higher hinge pin height; "
            "maintains reach at elevation; preferred for truck loading and "
            "rack-height material handling; explicitly state 'vertical lift' in title"
        )
    elif lift == "radial":
        highlights.append(
            "[NOTE] Radial Lift -- better breakout at grade; suited for digging, "
            "grading, concrete flatwork, and demolition; lower max dump height than vertical"
        )

    # High flow (Rank 4)
    if hf is True and hf_gpm:
        highlights.append(
            f"[HIGH VALUE] High-Flow Hydraulics ({hf_gpm:.0f} GPM) -- "
            "call out explicitly; enables cold planer, brush cutter, "
            "stump grinder -- opens premium attachment buyer market"
        )
    elif hf_unconf:
        highlights.append(
            "[HIGH VALUE] High-Flow Hydraulics (GPM unconfirmed) -- "
            "call out in listing; verify GPM from spec sheet before advertising"
        )
    elif hf is False:
        highlights.append(
            "[NOTE] No High-Flow -- positions machine for standard attachment work; "
            "buyers needing cold planer or mulcher will look elsewhere"
        )

    # Enclosed cab (Rank 5)
    if cab is True:
        highlights.append(
            "[VALUE] Enclosed Cab -- all-weather use, debris protection, "
            "HVAC comfort; valued $3,000-8,000 premium over open ROPS"
        )
    elif cab is False:
        highlights.append(
            "[NOTE] ROPS (Open Cab) -- limits buyer pool for commercial snow, "
            "demolition, and all-day outdoor operators; note any fan or canopy additions"
        )

    # 2-speed (Rank 6)
    if two_spd is True:
        highlights.append(
            "[VALUE] 2-Speed -- road-speed travel between positions; "
            "essential for commercial snow removal, large-area work"
        )
    elif two_spd is False:
        highlights.append(
            "[NOTE] Single Speed -- flag for snow removal and multi-zone buyers; "
            "note top speed if known"
        )

    # Ride control (Rank 7)
    if ride is True:
        highlights.append(
            "[VALUE] Ride Control -- reduced bucket spill at travel speed; "
            "operator comfort; worth noting in listing"
        )

    # Joystick controls (Rank 8)
    if joystick is True:
        highlights.append(
            "[VALUE] Joystick Controls -- faster operator learning curve; "
            "ergonomic premium; note if ISO or H-pattern for operators switching machines"
        )

    # Tire condition (Rank 9)
    if tire_pct is not None:
        if tire_pct >= 80:
            highlights.append(
                f"[VALUE] Good Tires ({int(tire_pct)}%) -- "
                "call out proactively; buyers factor tire replacement cost immediately"
            )
        elif tire_pct >= 50:
            highlights.append(
                f"[NOTE] Moderate Tires ({int(tire_pct)}%) -- "
                "buyer will inspect; describe accurately in listing"
            )
        else:
            highlights.append(
                f"[CAUTION] Worn Tires ({int(tire_pct)}%) -- "
                "buyers will price this in; disclose upfront or price accordingly; "
                "typical replacement $800-2,500 per set depending on size"
            )

    # Strong ROC (Rank 10)
    if roc and roc >= 2800:
        highlights.append(
            f"[VALUE] High ROC ({int(roc):,} lb) -- "
            "strong lift capacity; feature for production and loading buyers"
        )

    # Strong HP
    if hp and hp >= 88:
        highlights.append(
            f"[VALUE] High HP ({int(hp)} HP) -- "
            "capable of sustained production-rate attachment work; "
            "mention alongside high-flow if equipped"
        )

    return highlights


