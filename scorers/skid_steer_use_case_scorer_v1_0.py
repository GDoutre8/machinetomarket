# MACHINE PROFILE DETECTION
# ---------------------------------------------------------------------------

def _compute_machine_profile(record: MachineRecord) -> str:
    """
    Detect the dominant buyer profile for this machine based on spec signals.

    production:  high flow + enclosed cab + 2-speed all confirmed True
                 → boost truck loading, material handling, production work
    yard:        open cab + 2-speed (or open cab + high hours > 3,000)
                 → boost warehouse/yard use
    ag:          dealer_type contains ag/farm/ranch signal
                 → boost agriculture/farm use with SSL-specific descriptor
    general:     default — no strong profile signals

    Note: profiles are not mutually exclusive in theory, but the priority
    order (production → yard → ag → general) ensures a single best-fit label.
    """
    hf      = _safe_get(record, "high_flow_available")
    cab     = _safe_get(record, "enclosed_cab_available")
    two_spd = _safe_get(record, "two_speed_available")
    hours   = _safe_get(record, "hours") or 0
    dealer  = (_safe_get(record, "dealer_type") or "").lower()

    # Production: all three production signals present
    if hf is True and cab is True and two_spd is True:
        return "production"

    # Yard: open cab is the defining signal
    if cab is False and (two_spd is True or hours > 3000):
        return "yard"

    # Ag: dealer type carries farm/ag keyword
    ag_keywords = ["ag", "farm", "agricultural", "ranch"]
    if any(kw in dealer for kw in ag_keywords):
        return "ag"

    return "general"


